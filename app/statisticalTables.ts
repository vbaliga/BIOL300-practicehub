function getNearestFDf(df: number, table: Record<number, Record<number, number>>): number {
  const keys = Object.keys(table).map(Number).sort((a, b) => a - b);
  let best = keys[0];
  for (const k of keys) {
    if (k <= df) best = k;
    else break;
  }
  return best;
}

function getNearestFDf2(df2: number, row: Record<number, number>): number {
  const keys = Object.keys(row).map(Number).sort((a, b) => a - b);
  let best = keys[0];
  for (const k of keys) {
    if (k <= df2) best = k;
    else break;
  }
  return best;
}

export function getFCritical(df1: number, df2: number, alpha: 0.05 | 0.01): number {
  const table = alpha === 0.05 ? F_CRITICAL_05 : F_CRITICAL_01;
  const nearDf1 = getNearestFDf(df1, table);
  const row = table[nearDf1];
  if (!row) return NaN;
  const nearDf2 = getNearestFDf2(df2, row);
  return row[nearDf2] ?? NaN;
}

export function getFPRange(fStat: number, df1: number, df2: number): string {
  const f05 = getFCritical(df1, df2, 0.05);
  const f01 = getFCritical(df1, df2, 0.01);
  if (fStat >= f01) return "P < 0.01";
  if (fStat >= f05) return "0.01 < P < 0.05";
  return "P > 0.05";
}

// ─── HELPERS ─────────────────────────────────────────────────────────────────

export function round(x: number, decimals = 3): number {
  const factor = Math.pow(10, decimals);
  return Math.round(x * factor) / factor;
}

export function fmt(x: number, decimals = 3): string {
  return x.toFixed(decimals);
}
