const T_ALPHA_LEVELS = [0.20, 0.10, 0.05, 0.02, 0.01, 0.001];
const T_ALPHA_LABELS = ["P > 0.20", "0.10 < P < 0.20", "0.05 < P < 0.10", "0.02 < P < 0.05", "0.01 < P < 0.02", "0.001 < P < 0.01", "P < 0.001"];

function getNearestTDf(df: number): number {
  const keys = Object.keys(T_CRITICAL).map(Number).sort((a, b) => a - b);
  // Find closest df without exceeding (conservative)
  let best = keys[0];
  for (const k of keys) {
    if (k <= df) best = k;
    else break;
  }
  return best;
}

export function lookupTCritical(df: number, alphaTwoTailed: number): number {
  const nearDf = getNearestTDf(df);
  const row = T_CRITICAL[nearDf];
  const idx = T_ALPHA_LEVELS.indexOf(alphaTwoTailed);
  if (idx === -1) return NaN;
  return row[idx];
}

export function getTwoTailedPRange(tStat: number, df: number): string {
  const absT = Math.abs(tStat);
  const nearDf = getNearestTDf(df);
  const row = T_CRITICAL[nearDf];
  for (let i = 0; i < row.length; i++) {
    if (absT < row[i]) return T_ALPHA_LABELS[i];
  }
  return T_ALPHA_LABELS[T_ALPHA_LABELS.length - 1];
}

export function getOneTailedPRange(tStat: number, df: number): string {
  // For one-tailed tests, halve the two-tailed alpha
  const range = getTwoTailedPRange(tStat, df);
  return range
    .replace("P > 0.20", "P > 0.10")
    .replace("0.10 < P < 0.20", "0.05 < P < 0.10")
    .replace("0.05 < P < 0.10", "0.025 < P < 0.05")
    .replace("0.02 < P < 0.05", "0.01 < P < 0.025")
    .replace("0.01 < P < 0.02", "0.005 < P < 0.01")
    .replace("0.001 < P < 0.01", "0.0005 < P < 0.005")
    .replace("P < 0.001", "P < 0.0005");
}

